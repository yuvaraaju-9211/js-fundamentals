/** Rules and Naming Convention for variables */

// 1. Case Sensitive
let year = 2024;
let Year = 2024;

// 2. Contains Alphabets(aA-zZ), Numbers(0-9), Underscore(_), Dollar($)
let name = "Yuva";
let firstName = "Yuva"; // or Firstname
let first_name = "Yuva"; // or _first_name
let firstname$ = "Yuva"; // or $firstname
let name1 = "Yuva";

// 3. Cannot start with a number
let num1 = 100; // 1num is not allowed
let _num = 100;
let $num = 100;

// 4. No Reserved Keywords
// let let = 50;

// 5. No Blank Spaces in between
// let birth date = 112024;

// 6. Under 10 to 15 characters in length for readability


// 7. Variable names should always exist on the left hand side
let p = 25;
// let 25 = p;

